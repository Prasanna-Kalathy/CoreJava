package com.pk.core.Switch;

public class Switch {
	public static void main(String[] args) {
		String s = "98";
		switch (s) {
		case ("95"): {
			System.out.println("95");
			break;
		}
		case ("98"): {
			System.out.println("98");
			break;
		}
		default: {
			System.out.println("None");
		}

		}
	}
}
